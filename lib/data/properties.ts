export const PROPERTIES: Property[] = RAW_PROPERTIES.map((p) => ({
  ...p,
  cover: COVERS[p.id] ?? coverPhoto(p.images[0]),
  regionProfile:
    REGION_PROFILES[p.district] ??
    REGION_PROFILES[p.neighborhood ?? ''] ??
    DEFAULT_PROFILE,
  nearby: defaultPOI(p),
}));

export const getProperty = (idOrSlug: string) =>
  PROPERTIES.find((p) => p.id === idOrSlug || p.slug === idOrSlug);

export const publicProperties = () => PROPERTIES.filter((p) => !p.isPrivate);
export const privateProperties = () => PROPERTIES.filter((p) => p.isPrivate);
